def extract(p):
    full_path = os.path.join(base_path, p)
    print("Reading", full_path)
    if not os.path.exists(full_path):
        return []
        
    with open(full_path, 'r', encoding='utf-8', errors='ignore') as f:
        content = f.read()
        
    # The payload MUST contain [{"jsonb_agg": ... }]
    # We regex exactly that part
    # Look for literally the array notation
    # In python, we can just find where jsonb_agg begins
    idx = content.find('"jsonb_agg"')
    if idx == -1:
        print("No jsonb_agg string found in file")
        return []
        
    # go backwards to find the `[`
    start_idx = content.rfind('[', 0, idx)
    # go forwards to find the `]` which closes the array. 
    # But wait, it's inside a string representation. So it's actually literal `[` inside a JSON string.
    # We can just unescape the entire content if it's a valid JSON string
    obj = None
    try:
        obj = json.loads(content)
    except: pass
    
    if obj and "result" in obj:
        text = obj["result"]
    else:
        text = content
        
    match = re.search(r'(\[\s*\{\s*"jsonb_agg"[\s\S]*?\}\s*\])', text)
    if match:
        arr_str = match.group(1)
        try:
            parsed = json.loads(arr_str)
            if 'jsonb_agg' in parsed[0] and parsed[0]['jsonb_agg']:
                print(f"Extracted {len(parsed[0]['jsonb_agg'])} items")
                return parsed[0]['jsonb_agg']
        except Exception as e:
            print("Failed to parsed array string:", e)
            print("Array str started with:", arr_str[:50])
    
    return []

